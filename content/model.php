    <div class="col s12 center">
        <p>The core of the model is the ticket table, which has the following zones:</p>
    </div>
    <div class="col s12 left">
        <ul>
            <li>
                <p>
                    <b>Location:</b>
                    Include a complete direction of the cinema and general type of contacts as Phone, Email, WhatsApp, etc.
                </p>
            </li>

            <li>
                <p>
                    <b>Staff:</b>
                    Personal Information about employees. Moreover, people table will help to add rewards cards in the future.
                </p>
            </li>

            <li>
                <p>
                    <b>Payment:</b>
                    Standardized information about payments (card, cash, etc).
                </p>
            </li>

            <li>
                <p>
                    <b>Product:</b>
                    Normalized product metadata as <i>family</i> (ticket or food), <i>class</i> (general type of tickets and food) and <i>category</i> (specific type). For example, Matinee category belongs to discount ticket class and ticket family.
                </p>
            </li>

            <li>
                <p>
                    <b>Film:</b>
                    Movies will have multiple genres, cast and pictures.
                </p>
            </li>

            <li>
                <p>
                    <b>Film Session:</b>
                    Film tickets are created to access to the movie. A session belongs to a unique seat, room, cinema and could be displayed in a different language rather than the original.
                </p>
            </li>

            <li>
                <p>
                    <b>Ticket:</b>
                    Ticket table will contain more than one item. This help to calculate revenue.
                </p>
            </li>

        </ul>
    </div>
